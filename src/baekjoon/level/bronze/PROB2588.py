#            2 3 6 0 - (3) = C
#          3 7 7 6   - (4) = D
#        1 4 1 6     - (5) = E
#        -----------------
#        1 8 1 7 2 0 - (6) = F

#        (3), (4), (5), (6) 에 들어갈 값을 출력하세요.


A = int(input())
B = input()

C = A * int(B[2])
D = A * int(B[1])
E = A * int(B[0])

F = C + (D * 10) + (E * 100)

print(C, D, E, F, sep = '\n')